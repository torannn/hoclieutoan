/* =========================================================
 * JSX Editor — EXPORT
 * - Scene JSON (fits preset=scene in assets/js/diagrams.js) — only for primitives.
 * - JSXGraph raw JS code — supports all construction commands.
 * - HTML snippet (data-jxg='...')
 * ========================================================= */
(function (global) {
  'use strict';

  const Core = global.JSXEditor.Core;

  const SCENE_SUPPORTED = new Set(['point', 'segment', 'polygon', 'circle_cp', 'circle_cr']);

  function canUseScene(log) {
    return log.every(c => SCENE_SUPPORTED.has(c.kind));
  }

  // ================== SCENE JSON ==================
  function toSceneJSON(opts) {
    const log = Core.getLog();
    const board = Core.getBoardOpts();
    const spec = {
      preset: 'scene',
      bbox: board.bbox,
      height: board.height || 260,
      grid: !!board.grid,
      axes: board.axis !== false,
      keepAspectRatio: board.keepAspectRatio !== false,
      points: {},
      segments: [],
      polygons: [],
      circles: []
    };
    // free points (point kind)
    log.forEach(c => {
      if (c.kind === 'point') {
        const s = c.style || {};
        spec.points[c.id] = {
          xy: [round(c.args[0]), round(c.args[1])],
          name: s.label !== undefined ? s.label : c.id,
          color: s.color || '#dc2626',
          size: s.size || 3,
          visible: s.visible !== false
        };
      }
    });
    log.forEach(c => {
      const s = c.style || {};
      if (c.kind === 'segment') {
        spec.segments.push({
          p1: c.args[0], p2: c.args[1],
          color: s.color || '#2563eb',
          strokeWidth: s.strokeWidth || 2,
          dash: s.dash || 0
        });
      } else if (c.kind === 'polygon') {
        spec.polygons.push({
          points: c.args.slice(),
          color: s.color || '#0f172a',
          strokeWidth: s.strokeWidth || 2,
          fillOpacity: s.fillOpacity !== undefined ? s.fillOpacity : 0.08,
          fillColor: s.fillColor || '#3b82f6'
        });
      } else if (c.kind === 'circle_cp') {
        spec.circles.push({
          center: c.args[0], through: c.args[1],
          color: s.color || '#2563eb',
          strokeWidth: s.strokeWidth || 2
        });
      } else if (c.kind === 'circle_cr') {
        spec.circles.push({
          center: c.args[0], r: c.args[1],
          color: s.color || '#2563eb',
          strokeWidth: s.strokeWidth || 2
        });
      }
    });
    return spec;
  }

  function toSceneJSONString() {
    const spec = toSceneJSON();
    return JSON.stringify(spec, null, 2);
  }

  // ================== HTML SNIPPET ==================
  function toHtmlSnippet() {
    const spec = toSceneJSON();
    // Use single-quote outer, escape single quotes in JSON (shouldn't appear)
    const j = JSON.stringify(spec);
    return `<div class="jxg-diagram" data-jxg='${j.replace(/'/g, '&#39;')}'></div>`;
  }

  // ================== JSXGRAPH RAW CODE ==================
  function toJSXCode() {
    const log = Core.getLog();
    const board = Core.getBoardOpts();
    const lines = [];
    lines.push('// Generated by JSX Editor');
    lines.push('// Paste into a page that has loaded jsxgraphcore.js + jsxgraph.css');
    lines.push('const board = JXG.JSXGraph.initBoard("jxgbox", {');
    lines.push(`  boundingbox: [${board.bbox.join(', ')}],`);
    lines.push(`  axis: ${board.axis !== false},`);
    lines.push(`  grid: ${!!board.grid},`);
    lines.push(`  keepAspectRatio: ${board.keepAspectRatio !== false},`);
    lines.push('  showCopyright: false,');
    lines.push('  showNavigation: true');
    lines.push('});');
    lines.push('');

    log.forEach(c => {
      const s = c.style || {};
      const attrs = [];
      // Common
      if (Core.isPointKind(c.kind)) {
        const name = s.label !== undefined ? s.label : c.id;
        if (name) attrs.push(`name: ${JSON.stringify(name)}`);
        attrs.push(`size: ${s.size || 3}`);
        attrs.push(`strokeColor: ${JSON.stringify(s.color || '#dc2626')}`);
        attrs.push(`fillColor: ${JSON.stringify(s.color || '#dc2626')}`);
        if (s.visible === false) attrs.push('visible: false');
      } else if (c.kind === 'polygon') {
        attrs.push(`borders: { strokeColor: ${JSON.stringify(s.color || '#0f172a')}, strokeWidth: ${s.strokeWidth || 2} }`);
        attrs.push(`fillColor: ${JSON.stringify(s.fillColor || '#3b82f6')}`);
        attrs.push(`fillOpacity: ${s.fillOpacity !== undefined ? s.fillOpacity : 0.08}`);
        attrs.push('hasInnerPoints: false');
        attrs.push('vertices: { visible: false }');
      } else {
        attrs.push(`strokeColor: ${JSON.stringify(s.color || '#2563eb')}`);
        attrs.push(`strokeWidth: ${s.strokeWidth || 2}`);
        if (s.dash) attrs.push(`dash: ${s.dash}`);
        if (s.visible === false) attrs.push('visible: false');
      }
      const attrObj = attrs.length ? `{ ${attrs.join(', ')} }` : '{}';

      switch (c.kind) {
        case 'point':
          lines.push(`const ${c.id} = board.create('point', [${c.args[0]}, ${c.args[1]}], ${attrObj});`);
          break;
        case 'segment':
          lines.push(`const ${c.id} = board.create('segment', [${c.args[0]}, ${c.args[1]}], ${attrObj});`);
          break;
        case 'line':
          lines.push(`const ${c.id} = board.create('line', [${c.args[0]}, ${c.args[1]}], ${attrObj});`);
          break;
        case 'ray':
          lines.push(`const ${c.id} = board.create('line', [${c.args[0]}, ${c.args[1]}], Object.assign(${attrObj}, { straightFirst: false, straightLast: true }));`);
          break;
        case 'polygon':
          lines.push(`const ${c.id} = board.create('polygon', [${c.args.join(', ')}], ${attrObj});`);
          break;
        case 'circle_cp':
          lines.push(`const ${c.id} = board.create('circle', [${c.args[0]}, ${c.args[1]}], ${attrObj});`);
          break;
        case 'circle_cr':
          lines.push(`const ${c.id} = board.create('circle', [${c.args[0]}, ${c.args[1]}], ${attrObj});`);
          break;
        case 'midpoint':
          lines.push(`const ${c.id} = board.create('midpoint', [${c.args[0]}, ${c.args[1]}], ${attrObj});`);
          break;
        case 'perpendicular':
          // args: [pointId, lineId]
          lines.push(`const ${c.id} = board.create('perpendicular', [${c.args[1]}, ${c.args[0]}], ${attrObj});`);
          break;
        case 'parallel':
          lines.push(`const ${c.id} = board.create('parallel', [${c.args[1]}, ${c.args[0]}], ${attrObj});`);
          break;
        case 'intersect':
          lines.push(`const ${c.id} = board.create('intersection', [${c.args[0]}, ${c.args[1]}, ${c.args[2] || 0}], ${attrObj});`);
          break;
        case 'tangent': {
          // Use polarline trick:
          lines.push(`// ${c.id}: tangent from point ${c.args[0]} to circle ${c.args[1]} (branch ${c.args[2] || 1})`);
          lines.push(`const ${c.id}_pol = board.create('polarline', [${c.args[1]}, ${c.args[0]}], { visible: false });`);
          const idx = (c.args[2] === 2) ? 1 : 0;
          lines.push(`const ${c.id}_T = board.create('intersection', [${c.id}_pol, ${c.args[1]}, ${idx}], { visible: false });`);
          lines.push(`const ${c.id} = board.create('line', [${c.args[0]}, ${c.id}_T], ${attrObj});`);
          break;
        }
      }
    });
    return lines.join('\n');
  }

  // ================== LOG VIEW (GeoGebra-like listing) ==================
  function formatLogLine(c) {
    const name = c.id;
    const s = (c.style && c.style.label !== undefined) ? c.style.label : c.id;
    const nicename = Core.isPointKind(c.kind) ? s : name;
    switch (c.kind) {
      case 'point': return `${nicename} = Point(${round(c.args[0])}, ${round(c.args[1])})`;
      case 'segment': return `${name} = Segment(${c.args[0]}, ${c.args[1]})`;
      case 'line': return `${name} = Line(${c.args[0]}, ${c.args[1]})`;
      case 'ray': return `${name} = Ray(${c.args[0]}, ${c.args[1]})`;
      case 'polygon': return `${name} = Polygon(${c.args.join(', ')})`;
      case 'circle_cp': return `${name} = Circle(${c.args[0]}, through ${c.args[1]})`;
      case 'circle_cr': return `${name} = Circle(${c.args[0]}, r=${c.args[1]})`;
      case 'midpoint': return `${nicename} = Midpoint(${c.args[0]}, ${c.args[1]})`;
      case 'perpendicular': return `${name} = Perpendicular(${c.args[0]}, ⊥${c.args[1]})`;
      case 'parallel': return `${name} = Parallel(${c.args[0]}, ∥${c.args[1]})`;
      case 'intersect': return `${nicename} = Intersect(${c.args[0]}, ${c.args[1]})${c.args[2] ? '·'+c.args[2] : ''}`;
      case 'tangent': return `${name} = Tangent(${c.args[0]}, ${c.args[1]})`;
    }
    return `${name} = ${c.kind}(…)`;
  }

  function round(v) {
    if (typeof v !== 'number') return v;
    return Math.round(v * 10000) / 10000;
  }

  // ================== PUBLIC ==================
  global.JSXEditor.Export = {
    toSceneJSON, toSceneJSONString, toHtmlSnippet, toJSXCode,
    canUseScene, formatLogLine
  };
})(window);
